/*******************************************************************************
* Function Name  : TIM2_IRQHandler
* Description    : This function handles TIM2 global interrupt request.
* Input          : None
* Output         : None
* Return         : None
*******************************************************************************/
void TIM2_IRQHandler(void)
{
  static u8 gapFound = 0;

  if (TIM_GetITStatus(TIM2, TIM_IT_CC3) != RESET)
  {
    capture = TIM_GetCapture3(TIM2);
    TIM2->CNT = 0;
    if (gapFound == 0) pwm1Index = 1;
    if ((gapFound) && (capture < 9000)) 
    {
      if (capture > 4640)
        pwm1[pwm1Index++] = capture - 4640;  //subtract off 2.2us
      else if (capture < 2000)   //Too small - reset
        gapFound = 0;
      else
        pwm1[pwm1Index++] = 1;  
      if (pwm1Index == 6) pwmStatusFlags |= RX1_INTRDY;
      if (pwm1Index >= 7) gapFound = 0;
    }
    if ((capture > 10000) && (gapFound == 0)) gapFound = 1;
    TIM_ClearITPendingBit(TIM2, TIM_IT_CC3);
  }
}

